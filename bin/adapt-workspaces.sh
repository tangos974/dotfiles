#!/usr/bin/env bash
set -euo pipefail

HYPR_OUT="${HOME}/.config/hypr/workspace-layout.generated.conf"
WAYBAR_OUT="${HOME}/.config/waybar/workspaces.generated.jsonc"

mkdir -p "$(dirname "$HYPR_OUT")" "$(dirname "$WAYBAR_OUT")"

python3 <<'PY'
import json
import subprocess
from pathlib import Path

HYPR_OUT = Path.home() / ".config/hypr/workspace-layout.generated.conf"
WAYBAR_OUT = Path.home() / ".config/waybar/workspaces.generated.jsonc"

# Get monitors from Hyprland, sorted left -> right
monitors = json.loads(subprocess.check_output(["hyprctl", "-j", "monitors"], text=True))
monitors = [m for m in monitors if not m.get("disabled", False)]
monitors.sort(key=lambda m: m["x"])

names = [m["name"] for m in monitors]
if not names:
    raise SystemExit("No monitors found")

TOTAL = 10

# Distribute remainder to the RIGHTMOST monitors
# 3 monitors -> 3,3,4
# 2 monitors -> 5,5
# 4 monitors -> 2,2,3,3
base = TOTAL // len(names)
rem = TOTAL % len(names)
counts = [base] * len(names)
for i in range(rem):
    counts[-(i + 1)] += 1

assignment = {}
ws = 1
for mon, count in zip(names, counts):
    assignment[mon] = list(range(ws, ws + count))
    ws += count

# Hyprland generated config
lines = [
    "# AUTOGENERATED - DO NOT EDIT\n",
    "# workspace -> monitor assignment\n",
]

for idx, mon in enumerate(names):
    workspaces = assignment[mon]
    for j, w in enumerate(workspaces):
        parts = [f"workspace = {w}", f"monitor:{mon}", "persistent:true"]
        if j == 0:
            parts.append("default:true")
        lines.append(", ".join(parts) + "\n")

HYPR_OUT.write_text("".join(lines))

# Waybar generated module override
# This assumes your main config already uses "hyprland/workspaces" in modules-left
waybar = {
    "hyprland/workspaces": {
        "format": "{name}",
        "all-outputs": False,
        "sort-by-number": True,
        "persistent-workspaces": assignment
    }
}

WAYBAR_OUT.write_text(json.dumps(waybar, indent=2) + "\n")

print("Generated assignment:")
for mon in names:
    print(f"  {mon}: {assignment[mon]}")
PY

hyprctl reload >/dev/null 2>&1 || true
# Note: waybar restart is handled by watch-monitor-events.sh on monitor changes.
# On initial boot, waybar starts after this script generates the config, so no restart needed.
